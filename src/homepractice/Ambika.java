package homepractice;

public class Ambika {
	public static void main(String[] args) {
		System.out.println("   *   ");
		System.out.println("  ***  ");
		System.out.println(" *****");
		System.out.println("*******");
		
		int a=9;
		
		int b=0;
		
		b=9;
		b=11;
		b=56;
		b=0;
		
		System.out.println("a"); //a
		System.out.println(a); //9
		System.out.println(b); //0
		
		char c='f';
		c='k';
		System.out.println("c");//c
		System.out.println(c);//k
		
		
		long t=789;
		System.out.println(t);//789
		
		boolean x=true;
		boolean y=false;
		System.out.println(x);//true
		System.out.println(y);//false;
		
		float p=3.4f;
		System.out.println(p);//3.4
		
		double s=4.56;
		System.out.println(s);//4.56
		
		short r=45;
		System.out.println(r);//45
		
		
	}
	

}
